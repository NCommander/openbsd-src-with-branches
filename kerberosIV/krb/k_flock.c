#include "krb_locl.h"

#define K_OP_MASK (K_LOCK_SH | K_LOCK_EX | K_LOCK_UN)

int
k_flock(int fd, int operation)
{
  int op = 0;
  if (operation & K_LOCK_SH)
    op |= LOCK_SH;
  if (operation & K_LOCK_EX)
    op |= LOCK_EX;
  if (operation & K_LOCK_UN)
    op |= LOCK_UN;
  if (operation & K_LOCK_NB)
    op |= LOCK_NB;

  return flock(fd, op);
}
