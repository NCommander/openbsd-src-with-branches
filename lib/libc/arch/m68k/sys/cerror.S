#include "SYS.h"

WEAK_ALIAS(_cerror,__cerror)

	.even
	.globl	_C_LABEL(errno)
__cerror:
#ifdef	__PIC__
	lea	%pc@(_GLOBAL_OFFSET_TABLE_@GOTPC),%a0
	movl	%a0@(_C_LABEL(errno)@GOT:w),%a0
	movl	%d0,%a0@
#else
	movl	%d0,_C_LABEL(errno)
#endif
	movl	#-1,%d0
	movl	#-1,%d1
	moveal	%d0,%a0
	rts
