#include <machine/asm.h>

#include "abi.h"

ENTRY(atan)
	RETGUARD_SETUP(atan, r11)
	XMM_ONE_ARG_DOUBLE_PROLOGUE
	fldl	ARG_DOUBLE_ONE
	fld1
	fpatan
	XMM_DOUBLE_EPILOGUE
	RETGUARD_CHECK(atan, r11)
	ret
END(atan)
